<p>Eu queria compartilhar com vocês que eu voltei a utilizar Whatsapp e todos os serviços da Google, e inclusive eu já reativei a minha assinatura no Youtube Premium e no Google One.</p> 
<p>Eu voltei a utilizar o Whatsapp pelo fato que eu quebrei o meu contrato do plano controle da Vivo para migrar para o Vivo Easy, justamente para economizar uma grana e por causa que as diárias compradas nunca se expiram caso não sejam usadas. Então eu acabei comprando logo de cara 440 diárias de Whatsapp, 130 diárias de Voz e SMS, 150 diárias de Twitter, 240 diárias de Spotify, 90 de Netflix e 120 de Youtube.</p>
<p>Eu realmente queria economizar entrando na onda de diárias mais eu acabei ficando viciado em comprar diárias e agora durante a Black Friday eu pretendo comprar os 100GB de internet no mesmo preço promocional da Black Friday do ano passado, que acabou custando R$ 199,99, tendo um cashback de R$ 99,99 para ser utilizado para comprar diárias em aplicativos ou até mesmo em mais internet. Eu comprando esses 100GB, poderei utilizá-lo por mais de 2 anos (4,16666667GB x 24 = 100GB) por causa que nos aplicativos que eu mais consumo internet eu tenho milhares de diárias e caso eu não tivesse essas diárias, eu poderia poderia consumir 8,8GB durante todos os meses por um ano (8,8GB x 12 = 100GB).</p>
<p>Em breve eu irei comprar mais 200 diárias de Voz e SMS, 360 diárias de Whatsapp, 200 diárias de Spotify e 150 de Youtube. Irei fazer isso para que eu possa ficar mais de um ano sem gastar nenhum centavo com telefonia móvel. Ainda vale lembrar que no meu antigo plano controle eu pagava todos os meses R$ 74,99, e em um ano eu apagava exatos R$ 899,88 em telefonia móvel, sendo que eu não utilizava mais que 50% meus benefícios que não eram acumulados.</p>
<p>No Vivo Easy cada 30 diárias de Whatsapp está custando R$ 29,90 mais no preço promocional está por R$ 9,90, Netflix custa R$ 29,90 tendo um cashback de R$ 14,95, Spotify R$ 19,90 com cashback de R$ 9,95, Youtube de R$ 89,70 por R$ 29,90 e sem cashback e no Twitter de R$ 29,70 por R$ 9,90 e sem nenhum cashback (os preços citados acima iram mudar com o passar do tempo).</p>
<p>Então o principal motivo que me fez voltar a utilizar o Whatsapp foi que eu acabei saindo da depressão que eu me encontrava e que as diárias do mesmo estão muito baratas, então eu vou tentar ao máximo utilizar o serviço e evitar evitar fazer ligações e SMS pela telefonia móvel. Já os serviços da Google por causa que eu tirei a paranoia da minha mente que eu estava sendo perseguido, porém isso realmente aconteceu por um período que eu me envolvi na política, porém eu continuava achando que isso vinha acontecendo por causa da depressão.</p>
