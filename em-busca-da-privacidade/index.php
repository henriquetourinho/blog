<h1>EM BUSCA DE PRIVACIDADE</h1>
<p>A cerca de um tempo eu vinha me perguntando porque eu deveria continuar sendo utilizado pelas redes sociais, governos e por grandes corporações, sendo que para minha pessoa não existia nenhum prazer e futilidade de querer ser mais uma cobaia das engenharias de comportamentos e das manipulações no mundo digital e físico, que a muito tempo são delineadas por engenheiros no mundo religoso e agora tendo como seus principais objetivos o total controle social, através do mundo digital.</p>
<p>Perante ao meu entender e a minha não precisão de visibilidade na surface, eu acabei decidindo excluir praticamente todas as minhas contas sociais, mantendo ainda apenas o mensageiro Whatsapp.</p>
<p>Por causa desse meu blackout social, algumas das pessoas que englobam meus meios de comunicações, acabaram se declinando sobre minha posição, mas eu reintegrei que não voltaria atrás sobre minhas delimitações sociais que aviam sido tomadas sobre um aspecto crítico da atual sociedade em que vivermos.</p>
<p>Nos dias de hoje eu quase não tenho links que ficam na front-end da surface e tenho um extremo cuidado com os meus dados pessoais, chegando deixar a maior parte dos meus dados criptografados em ambientesb totalmentes controlados, porém, eu não abrir mão da comodidade de deixar os backups dos meus dados em nuvens como o Google Drive.</p>

<p>Eu também troquei os serviços de e-mails tradicionais pelo Tutanota, que é alemão e que conta com as mais restritas leis de proteções a dados do mundo, tando que oferecem algumas das mais fortes proteções à privacidade do mundo, tanto para indivíduos quanto para corporações, além que a empresa está fora da jurisdição dos EUA, UE e do Brasil.</p>
